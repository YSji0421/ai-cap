    private void saveProduct(String type, String name, String provider, String desc,
                             String coverage, String keywords, String premium,
                             String ageRange, String riskCategories) {
        InsuranceProduct p = new InsuranceProduct();
        p.setInsuranceType(type);
        p.setProductName(name);
        p.setProvider(provider);
        p.setDescription(desc);
        p.setCoverageDetails(coverage);
        p.setTargetKeywords(keywords);
        p.setMonthlyPremiumRange(premium);
        p.setAgeRange(ageRange);
        p.setRiskCategories(riskCategories);
        productRepository.save(p);
    }

